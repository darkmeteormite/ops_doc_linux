date [OPTION]... [+FORMAT]
	+%s：显示时间戳
	+%A：显示周几
	+%D：显示月日年
	+%F：显示年月日
	+%T：显示当前时间
	+%H：显示小时
	+%M：显示分钟
	+%S：显示秒钟


clock



hwclock [OPTION]：显示硬件时钟
	-w，--systohc：同步系统时钟到硬件时钟
	-s，--hctosys：同步硬件时钟到系统时钟



